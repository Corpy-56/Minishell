#include "minishell.h"

int	ft_pwd(char *rl)
{
	char	*pwd;

	if (ft_strncmp(rl, "pwd", 4) == 0)
	{
		pwd = getcwd(NULL, 0);
		printf("%s\n", pwd);
	}
	return (0);
}
